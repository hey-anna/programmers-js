// 모스 부호

// 문제 요약:
// 공백으로 구분된 모스 부호 문자열(letter)이 주어질 때,
// 각 모스 부호를 알파벳 소문자로 변환하여
// 하나의 문자열로 반환한다.

// 풀이:
// 1. 모스 부호 표를 객체(morse)로 정의한다.
// 2. 입력 문자열(letter)을 공백 기준으로 split 하여 배열로 만든다.
// 3. 배열을 순회하며 각 모스 부호를 객체의 key로 사용해 알파벳으로 변환한다.
// 4. 변환된 문자 배열을 join("")으로 하나의 문자열로 합친다.

const solution = (letter) => {
  const morse = {
    ".-": "a",
    "-...": "b",
    "-.-.": "c",
    "-..": "d",
    ".": "e",
    "..-.": "f",
    "--.": "g",
    "....": "h",
    "..": "i",
    ".---": "j",
    "-.-": "k",
    ".-..": "l",
    "--": "m",
    "-.": "n",
    "---": "o",
    ".--.": "p",
    "--.-": "q",
    ".-.": "r",
    "...": "s",
    "-": "t",
    "..-": "u",
    "...-": "v",
    ".--": "w",
    "-..-": "x",
    "-.--": "y",
    "--..": "z",
  };
  const arr = letter.split(" ");
  return arr.map((code) => morse[code]).join("");
};
