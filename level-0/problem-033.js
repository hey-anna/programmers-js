// 개미 군단

// 장군개미: 공격력 5
// 병정개미: 공격력 3
// 일개미: 공격력 1

// 목표:
// 주어진 체력(hp)을 정확히 맞추면서
// 필요한 개미 수를 최소로 구한다.

// 플이:
// 공격력 대비 효율이 가장 좋은 개미부터 사용한다.
// (5 → 3 → 1 순서의 그리디 방식)

const solution = (hp) => {
  let count = 0;

  // 장군개미(5) 최대한 사용
  count += Math.floor(hp / 5);
  hp %= 5;

  // 병정개미(3) 최대한 사용
  count += Math.floor(hp / 3);
  hp %= 3;

  // 남은 체력은 일개미(1)로 처리
  count += hp;

  return count;
};
