// 구슬을 나누는 경우의 수

// 문제
// 구슬은 모두 서로 다르게 생겼다.
// 순서는 고려하지 않는다.
// balls개의 구슬 중 share개의 구슬을 고르는
// 가능한 모든 경우의 수를 구한다.

// 조합(Combination) 문제
// nCr : n개 중 r개를 고르는 경우의 수

// n = balls
// r = share

// 조합(nCr) 공식
// nCr = n! / (r! × (n - r)!)
// 예: 5C3 = 5! / (3! * (5-3)!)

// 계산은 반복문을 이용해 단계별로 처리하며,
// 팩토리얼을 직접 계산하지 않고
// 필요한 값만 곱하고 나누는 방식이다.

const solution = (balls, share) => {
  let result = 1;

  for (let i = 1; i <= share; i++) {
    result = (result * (balls - i + 1)) / i;
  }

  return result;
};

// 예시: 5C3

// 초기값
// result = 1

// i = 1
// (1 * (5 - 1 + 1)) / 1 = 5

// i = 2
// (5 * (5 - 2 + 1)) / 2 = 10

// i = 3
// (10 * (5 - 3 + 1)) / 3 = 10

// 최종 결과: 10
